    <h1>Plan, track and adapt</h1>
    <p>Soy David González, comparto mi vida con mi pareja, nuestras dos hijas y un perro que está como una cabra. Para pagarme aquellas cosas que compra el dinero trabajo como ingeniero de software. Soy un maniático del carajo y me gusta que las cosas funcionen bien.</p>
    <p>Vivo en Donostia, estudié Ingeniería Técnica en informática de sistemas, he trabajado en (cronológicamente): <a href="http://arista.es" alt="Arista interactiva">Arista interactiva</a>, <a href="http://diariovasco.com" alt="Diariovasco.com">diariovasco.com</a>, <a href="http://dvbus.info" alt="DVbus canal de información urbana">dvbus.info</a>, <a href="http://hispavista.com" alt="Hispavista">Hispavista</a>, <a href="http://binovo.es" alt="Binovo">Binovo</a>, <a href="http://nemsolutions.com" alt="NEM Solutions">NEM Solutions</a>, <a href="http://ticketbis.com" alt="Ticketbis">Ticketbis</a>, <a href="http://enigmedia.es" alt="Enigmedia website">Enigmedia</a>, <a href="https://xabet.net" alt="Xabet Digital Solutions website">Xabet</a>, <a href="https://sysdig.com" alt="Sysdig website">Sysdig</a> y actualmente trabajo en <a href="https://www.lifullconnect.com/" alt="Lifull connect website">Lifull Connect</a>. Cuando puedo ayudo a <a href="http://twitter.com/mallimi">@mallimi</a> en su aventura <a href="http://buzzko.com">Buzzko Marketing</a>.</p>
    <p>Creo que en el desarrollo de productos de software la palabra clave es productos. Como tal creo que incluso en las discusiones mas puramente técnicas dicho aspecto no debe de olvidarse. Preguntarnos contínuamente por qué hacemos algo y que va a aportar a nuestros usuarios/clientes es primordial.</p>
    <p>En mis ratos libres discuto, y mucho, con <a href="http://twitter.com/artolamola">@artolamola</a> y <a href="http://twitter.com/ggalmazor">@ggalmazor</a> en <a href="http://programania.net">programania</a>, nuestro "cuaderno de software" particular.</p>
